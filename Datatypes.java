import java.util.*;
class Datatypes
{
       static byte b;
       static boolean bool;
       static char ch;
       static int i;
public static void main(String args[])
{
      System.out.println("the default value of byte is"+b);
      System.out.println("the default value of boolean is"+bool);
      System.out.println("the default value of char is"+ch);
      System.out.println("the default value of int is"+i);
}
} 
